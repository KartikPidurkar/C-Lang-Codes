#include<stdio.h>
int main(){
    // Airthmetic ops + - * / %(modulous)
    // ++ (increment)
    // -- (decrement)
    // Augmented ops x+=2 is same as x=x+2
    // Augmented ops y-=2 is same as y=y+2
    // Augmented ops z*=2 is same as z=z*2
    // and the rest is same like the above
    int x=10;
    int y=10;
    x+=10;
    y-=8;
    printf("%d\n",x);
    printf("%d",y);

    return 0;
}